# 注意事项：
# 1.字符之间可以相加
# 2.同时打印两个字符串时，默认中间有空格
#   sep设置中间以什么符号相连接
#   end设置某位以什么方式结束
# 3.\n代表换行

print('Hello world')
print('Hello' + 'world')
print('Hello', 'world')
print('Hello', 'world', sep='')
print('Hello', 'world', sep=' ', end='\n')